#syntax - [start index : end index -1 ]
a = "python"
print(a[1:5])
print(a[ : ]) # print all string
print(a[5])
print(a[-5 : 5]) # [-5] = 'y' [5-1]='o'
print(a[1 : ]) # index[1] to [last-1] index or last position
print(a[ : 4]) # stsring [0] index to [4-1]index or 4 position
print(a[2 : 4])
print(a[4])
